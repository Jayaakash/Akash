//Remove an element from the element of an array
var a=['first','second','third'];
console.log("The value of a is",a);
var removed=a.shift();//shift is used to remove the element from the front of an array
console.log("The removed value is:",removed);




//Add an element from the front of an array
var a=['first','second','third'];
console.log("The value of a is",a);
a.unshift("zero");//unshift will add an element from the front of an array
console.log("The Zero value of a is added:",a);

